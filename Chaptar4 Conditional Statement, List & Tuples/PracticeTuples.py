# Create a tuple of marks (87, 64, 33, 95, 76) and print the highest and lowest marks using max() and min().

marks = (87, 64, 33, 95, 76)
minMarks = min(marks)
maxMarks = max(marks)
print("lowest",minMarks) #33
print("highest", maxMarks) #95

