# Comparison Operators

# Taking input
a = 10
b = 5

print("\n--- Arithmetic Operators ---")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a % b =", a % b)
print("a ** b =", a ** b)

print("\n--- Comparison Operators ---")
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)

print("\n--- Logical Operators ---")
print("a > 0 and b > 0:", a > 0 and b > 0)
print("a > 0 or b > 0:", a > 0 or b > 0)
print("not(a > 0):", not(a > 0))

print("\n--- Assignment Operators ---")
x = a

x += b
print("x += b:", x)

x -= b
print("x -= b:", x)

x *= b
print("x *= b:", x)

x /= b
print("x /= b:", x)

